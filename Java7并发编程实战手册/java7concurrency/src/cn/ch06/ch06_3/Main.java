import java.util.concurrent.PriorityBlockingQueue;
// 在范例中我们将大量不同优先级的事件存放到同一个列表中，并且检查队列是否按预期排序

// 13.创建范例主类Main，并添加main()方法

//使用按优先级排序的阻塞式线程安全列表
public class Main {

    public static void main(String[] args) {
        // 14.创建PriorityBlockingQueue对象，名为queue，并指定泛型参数是Event类
        PriorityBlockingQueue<Event> queue = new PriorityBlockingQueue<>();
        // 15.创建一个含有5个Thread对象的线程数组，用来存放将执行的5个任务的线程
        Thread[] taskThreads = new Thread[5];
        // 16.创建5个Task对象并作为传入参数创建线程，将创建的线程存入到上一步中创建的线程数组中
        for (int i = 0; i < taskThreads.length; i++) {
            Task task = new Task(i, queue);
            taskThreads[i] = new Thread(task);
        }
        // 17.启动上一步创建的5个线程
        for (int i = 0; i < taskThreads.length; i++) {
            taskThreads[i].start();
        }
        // 18.使用join()等待5个线程结束
        for (int i = 0; i < taskThreads.length; i++) {
            try {
                taskThreads[i].join();
            } catch (InterruptedException e) {
                e.printStackTrace();
            }
        }
        // 19.在控制台输出队列的实际大小和里面存放的events。使用poll()方法从队列中取出events
        System.out.printf("Main: Queue Size: %d\n", queue.size());
        for (int i = 0; i < taskThreads.length * 1000; i++) {
            Event event = queue.poll();
            System.out.printf("Thread %s: Priority %d\n", event.getThread(), event.getPriority());
        }
        // 20.在控制台输出一条表示程序运行结束的消息
        System.out.printf("Main: Queue Size: %d\n", queue.size());
        System.out.printf("Main: End of the program\n");
    }
}

